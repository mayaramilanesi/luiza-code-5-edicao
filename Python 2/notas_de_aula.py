# POO

# Principais pilares de POO

# Encapsulamento - Barreira protetora que impede que os dados e o código dentro de uma classe sejam acessados por outro código 
# Herança  - Processo onde uma classe adquire as propriedades e métodos de outra (simples, múltipla ou multinível)
# Polimorfismo - Permite que uma entidade como uma função ou objeto tenha mais de uma forma (ex mãe que tbm é médica)
# Abstração - Oculta os detalhes de implementação do usuário, apenas a informação sobre o que o objeto faz é fornecida

