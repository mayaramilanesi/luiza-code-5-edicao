# int
# Ex: 27, 500, 548451

idade = 27
ano = 1995

print(type(idade))
print(type(ano))


# Float (ponto flutuante)
# Ex: 1.75, 60.0

altura = 1.75
peso = 60.0

print(type(altura))
print(type(peso))

# boolean 
# Ex: True, False

semana = False
feriado = True

print(type(semana))
print(type(feriado))

# string
# Ex: 'hello', "word"

nome = 'Marcia'
profissão = 'Desenvolvedora de Software'

print(type(nome))
print(type(profissão))




# Variáveis
